# Give me the first number: 10
# Give me the second number: 2
# Thank you!
# 10 + 2 = 12
# 10 - 2 = 8
# 10 / 2 = 5
# 10 * 2 = 20

num1 = int(input("Give me the first number: "))
num2 = int(input("Give me the second number: "))
print("Thank you!")

print("%d + %d = %d" %(num1, num2, num1+num2))
print("%d - %d = %d" %(num1, num2, num1-num2))
print("%d / %d = %d" %(num1, num2, num1/num2))
print("%d * %d = %d" %(num1, num2, num1*num2))